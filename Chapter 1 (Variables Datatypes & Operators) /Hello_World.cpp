#include <iostream> 
using namespace std;

int main() {
    cout << "Hello World ";
    cout << "Hello" <<endl; // It removes the % sign at the end 
    
    cout <<"Hello World"<<endl <<"Welcome Ansh"<<endl; // <<endl prints the next line character
    // NOTE : if we used << endl after welcome Ansh then it will remove the trailing % sign.
    
    // We can also use \n in place of <<endl
    cout << "Nehal" <<"\n";
    cout << "Nehal \n Dagar \n";
    
    return 0;
} 